class Myclass:
    def add(a,b):
        return a+b
    def sub(a,b):
        return a-b
    def mul(a,b):
        return a*b
    def div(a,b):
        return a/b
    def mod(a,b):
        return a%b
obj1=Myclass
print(obj1.add(2,3))
print(obj1.sub(3,2))
print(obj1.mul(5,5))
print(obj1.div(10,5))
print(obj1.mod(15,5))
